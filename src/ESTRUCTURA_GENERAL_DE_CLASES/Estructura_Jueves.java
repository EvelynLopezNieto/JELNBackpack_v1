
package ESTRUCTURA_GENERAL_DE_CLASES;


public class Estructura_Jueves {
    
    String hora_jueves, materia_jueves;

    public Estructura_Jueves() {
    }

    public Estructura_Jueves(String hora_jueves, String materia_jueves) {
        this.hora_jueves = hora_jueves;
        this.materia_jueves = materia_jueves;
    }

    public String getHora_jueves() {
        return hora_jueves;
    }

    public void setHora_jueves(String hora_jueves) {
        this.hora_jueves = hora_jueves;
    }

    public String getMateria_jueves() {
        return materia_jueves;
    }

    public void setMateria_jueves(String materia_jueves) {
        this.materia_jueves = materia_jueves;
    }
    
}
