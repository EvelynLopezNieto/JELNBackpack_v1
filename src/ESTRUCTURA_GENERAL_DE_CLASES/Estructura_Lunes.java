
package ESTRUCTURA_GENERAL_DE_CLASES;


public class Estructura_Lunes {
    
    String hora_lunes, materia_lunes;

    public Estructura_Lunes() {
    }

    public Estructura_Lunes(String hora_lunes, String materia_lunes) {
        this.hora_lunes = hora_lunes;
        this.materia_lunes = materia_lunes;
    }

    public String getHora_lunes() {
        return hora_lunes;
    }

    public void setHora_lunes(String hora_lunes) {
        this.hora_lunes = hora_lunes;
    }

    public String getMateria_lunes() {
        return materia_lunes;
    }

    public void setMateria_lunes(String materia_lunes) {
        this.materia_lunes = materia_lunes;
    }
    
    
}
