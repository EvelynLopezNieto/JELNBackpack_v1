
package ESTRUCTURA_GENERAL_DE_CLASES;


public class Estructura_Martes {
    
    String hora_martes, materia_martes;

    public Estructura_Martes() {
    }

    public Estructura_Martes(String hora_martes, String materia_martes) {
        this.hora_martes = hora_martes;
        this.materia_martes = materia_martes;
    }

    public String getHora_martes() {
        return hora_martes;
    }

    public void setHora_martes(String hora_martes) {
        this.hora_martes = hora_martes;
    }

    public String getMateria_martes() {
        return materia_martes;
    }

    public void setMateria_martes(String materia_martes) {
        this.materia_martes = materia_martes;
    }
    
}
