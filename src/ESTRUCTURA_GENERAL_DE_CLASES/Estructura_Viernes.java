
package ESTRUCTURA_GENERAL_DE_CLASES;


public class Estructura_Viernes {
    
    String hora_viernes, materia_viernes;

    public Estructura_Viernes() {
    }

    public Estructura_Viernes(String hora_viernes, String materia_viernes) {
        this.hora_viernes = hora_viernes;
        this.materia_viernes = materia_viernes;
    }

    public String getHora_viernes() {
        return hora_viernes;
    }

    public void setHora_viernes(String hora_viernes) {
        this.hora_viernes = hora_viernes;
    }

    public String getMateria_viernes() {
        return materia_viernes;
    }

    public void setMateria_viernes(String materia_viernes) {
        this.materia_viernes = materia_viernes;
    }
    
}
