
package ESTRUCTURA_GENERAL_DE_CLASES;


public class Estructura_Miercoles {
    
    String hora_miercoles, materia_miercoles;

    public Estructura_Miercoles() {
    }

    public Estructura_Miercoles(String hora_miercoles, String materia_miercoles) {
        this.hora_miercoles = hora_miercoles;
        this.materia_miercoles = materia_miercoles;
    }

    public String getHora_miercoles() {
        return hora_miercoles;
    }

    public void setHora_miercoles(String hora_miercoles) {
        this.hora_miercoles = hora_miercoles;
    }

    public String getMateria_miercoles() {
        return materia_miercoles;
    }

    public void setMateria_miercoles(String materia_miercoles) {
        this.materia_miercoles = materia_miercoles;
    }
    
}
