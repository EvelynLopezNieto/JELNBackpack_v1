package VENTANAS_PRINCIPALES;

public class Objetos {
    
    String ob1, ob2;

    public Objetos() {
    }

    public Objetos(String ob1, String ob2) {
        this.ob1 = ob1;
        this.ob2 = ob2;
    }

    public String getOb1() {
        return ob1;
    }

    public void setOb1(String ob1) {
        this.ob1 = ob1;
    }

    public String getOb2() {
        return ob2;
    }

    public void setOb2(String ob2) {
        this.ob2 = ob2;
    }
    
    
}
